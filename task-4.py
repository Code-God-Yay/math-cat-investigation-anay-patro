def print_star_triangle(height):
    """Print a triangle made of stars, getting smaller each line."""
    if height > 0:
        # Print stars for this line
        print('*' * height)
        # Then recursively do the same for one fewer star
        print_star_triangle(height - 1)

if __name__ == "__main__":
    # Ask the user how many lines they want
    number = int(input("How many lines for the star triangle? "))
    print_star_triangle(number)

 
